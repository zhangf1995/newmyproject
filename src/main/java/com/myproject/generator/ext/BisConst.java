package com.myproject.generator.ext;

public class BisConst {
	private Integer key;
	private String name;
	private String constField;
	public Integer getKey() {
		return key;
	}
	public void setKey(Integer key) {
		this.key = key;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getConstField() {
		return constField;
	}
	public void setConstField(String constField) {
		this.constField = constField;
	}
	
}
